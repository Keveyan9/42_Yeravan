#include <unistd.h>
#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int	n;

	n = 0;
	while (str[n])
	{
		if (!((str[n] >= 'a' && str[n] <= 'z')
				|| (str[n] >= 'A' && str[n] <= 'Z')))
			return (0);
		n++;
	}
	return (1);
}
