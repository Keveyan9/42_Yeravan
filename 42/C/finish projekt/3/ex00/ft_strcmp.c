int	ft_strcmp(char *s1, char *s2)
{
	int	n;

	n = 0;
	while (s1[n])
	{
		if (s1[n] > s2[n])
			return (1);
		if (s1[n] < s2[n])
			return (-1);
		n++;
	}
	if (s2[n])
		return (-1);
	return (0);
}	
