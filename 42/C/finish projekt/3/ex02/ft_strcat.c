char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	n;

	n = 0;
	i = 0;
	while (dest[i])
		i++;
	while (src[n])
	{
		dest[i] = src[n];
	i++;
	n++;
	}
	dest[i] = '\0';
	return (dest);
}
