#include<unistd.h>

char	*ft_strcpy(char	*dest, char *src)
{
	int	n;

	n = 0 ;
	while (src[n] != '\0')
	{
		dest[n] = src[n];
		n++;
	}
	dest[n] = '\0';
	return (dest);
}
